def load_weights(net, weights):
    ptr = 0
    for k, v in net.state_dict().items():
        tensor = v
        num = tensor.numel()
        w = torch.from_numpy(weights[ptr:ptr+num]).view_as(tensor)
        tensor.data.copy_(w)
        ptr += num


